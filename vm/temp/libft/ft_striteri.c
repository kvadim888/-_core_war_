#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char			*k;
	unsigned int	i;

	i = 0;
	if (s && f)
	{
		while (s[i])
		{
			k = &s[i];
			f(i, k);
			i++;
		}
	}
}
