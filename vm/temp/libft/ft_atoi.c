#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned long long	nbr;
	int					i;
	int					sign;

	i = 0;
	nbr = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i++] - '0');
		if (nbr > 9223372036854775807 && sign == 1)
			return (-1);
		if (nbr > 9223372036854775807 && sign == -1)
			return (0);
	}
	return (nbr * sign);
}
