#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putnbr(-214748);
		ft_putnbr(3648);
		n = -n;
	}
	if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	if (n != -2147483648)
		ft_putchar((n % 10) + '0');
}
