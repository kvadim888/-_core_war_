#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	unsigned int i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	while (*s2 != '\0')
	{
		s1[i++] = *s2;
		s2++;
	}
	s1[i] = *s2;
	return (s1);
}
